a = input('Digite algo: ')
print('O tipo primitivo desse valor é', type(a))
print('Esse valor é um número?', a.isnumeric())
print('Esse valor é alfabético?', a.isalpha())
print('Esse valor é alfanumérico?', a.isalnum())
print('Esse valor é maiúsculo?', a.isupper())
print('Esse valor é minúsculo?', a.islower())
print('Esse valor é captalizado?', a.istitle())






